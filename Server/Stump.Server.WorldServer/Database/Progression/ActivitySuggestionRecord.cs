using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.DofusProtocol.D2oClasses.Tools.D2o;
using System;
using Stump.Server.WorldServer.Database.I18n;

namespace Stump.Server.WorldServer.Database.Progression
{
    public class ActivitySuggestionRelator
    {
        public static string FetchQuery = "SELECT * FROM suggestion_template";
    }

    [TableName("suggestion_template")]
    [D2OClass("ActivitySuggestion", "com.ankamagames.dofus.datacenter.progression")]
    public class ActivitySuggestionRecord : IAssignedByD2O, IAutoGeneratedRecord
    {
        private string m_name;

        [PrimaryKey("Id", false)]
        public int Id { get; set; }

        public uint NameId { get; set; }

        public string Name
        {
            get { return m_name ?? (m_name = TextManager.Instance.GetText(NameId)); }
            set { m_name = value; }
        }

        public uint DescriptionId { get; set; }

        public int CategoryId { get; set; }

        public uint Level { get; set; }

        [NumericPrecision(20, 0)]
        public double MapId { get; set; }

        public Boolean IsLarge { get; set; }

        public long StartDate { get; set; }

        public long EndDate { get; set; }

        public string Icon { get; set; }

        #region IAssignedByD2O Members

        public void AssignFields(object d2oObject)
        {
            var activity = (DofusProtocol.D2oClasses.ActivitySuggestion)d2oObject;

            Id = activity.Id;
            NameId = activity.NameId;
            Name = "";
            DescriptionId = activity.DescriptionId;
            CategoryId = activity.CategoryId;
            Level = activity.Level;
            MapId = activity.MapId;
            IsLarge = activity.IsLarge;
            StartDate = (long)activity.StartDate;
            EndDate = (long)activity.EndDate;
            Icon = activity.Icon;
        }

        #endregion
    }
}