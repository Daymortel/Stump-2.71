using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;

namespace Stump.Server.WorldServer.Database.World
{
    public class CharactersItemsPaddockRelator
    {
        public static string FetchQuery = "SELECT * FROM characters_items_paddock";
    }

    [TableName("characters_items_paddock")]
    public sealed class CharactersItemsPaddock : AutoAssignedRecord<CharactersItemsPaddock>, IAutoGeneratedRecord
    {
        public int OwnerId { get; set; }

        public int PaddockId { get; set; }

        public long MapId { get; set; }

        public ushort ItemId { get; set; }

        public ushort CellId { get; set; }

        public short ItemEffectiveness { get; set; }

        public short ItemUseds { get; set; }

        public short ItemUsedsMax { get; set; }

        public void setItemUseds()
        {
            ItemUseds--;
            this.IsUpdate = true;
        }

        public void setNewCellId(ushort cellId)
        {
            this.CellId = cellId;
            this.IsUpdate = true;
        }

        public void setNew()
        {
            this.IsNew = true;
        }

        public void setUpdate()
        {
            this.IsUpdate = true;
        }

        public bool setDelete()
        {
            this.IsNew = false;
            this.IsUpdate  = false;
            this.IsDeleted = true;

            return true;
        }
    }
}