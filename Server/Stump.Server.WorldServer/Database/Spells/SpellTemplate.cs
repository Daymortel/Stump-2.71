using System;
using Stump.Core.IO;
using Stump.DofusProtocol.D2oClasses.Tools.D2o;
using Stump.DofusProtocol.D2oClasses;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Database.I18n;
using System.Linq;

namespace Stump.Server.WorldServer.Database.Spells
{
    public class SpellTemplateRelator
    {
        public static string FetchQuery = "SELECT * FROM spells_templates";
    }

    [TableName("spells_templates")]
    [D2OClass("Spell", "com.ankamagames.dofus.datacenter.spells")]
    public sealed class SpellTemplate : IAssignedByD2O, ISaveIntercepter, IAutoGeneratedRecord
    {
        private string m_description;
        private string m_name;
        private string m_spellLevelsIdsCSV;

        [PrimaryKey("Id", false)]
        public int Id { get; set; }

        public uint NameId { get; set; }

        public string Name
        {
            get { return m_name ?? (m_name = TextManager.Instance.GetText(NameId)); }
        }

        public uint DescriptionId { get; set; }

        public string Description
        {
            get { return m_description ?? (m_description = TextManager.Instance.GetText(DescriptionId)); }
        }

        public uint TypeId { get; set; }

        public uint Order { get; set; }

        public String ScriptParamsDataIds { get; set; }

        public String ScriptParamsCriticalIds { get; set; }

        public int ScriptId { get; set; }

        public int ScriptIdCritical { get; set; }

        public int IconId { get; set; }

        public string SpellLevelsIdsCSV
        {
            get { return m_spellLevelsIdsCSV; }
            set
            {
                m_spellLevelsIdsCSV = value;
                SpellLevelsIds = value.FromCSV<uint>(",");
            }
        }

        [Ignore]
        public uint[] SpellLevelsIds { get; set; }

        public bool UseParamCache { get; set; }

        public bool VerboseCast { get; set; }

        public string DefaultPreviewZone { get; set; }

        public bool BypassSummoningLimit { get; set; }

        public bool CanAlwaysTriggerSpells { get; set; }

        public bool HideCastConditions { get; set; }

        public string AdminName { get; set; }

        #region IAssignedByD2O Members

        public void AssignFields(object d2oObject)
        {
            var spell = (Spell)d2oObject;

            Id = spell.id;
            NameId = spell.nameId;
            DescriptionId = spell.descriptionId;
            TypeId = spell.typeId;
            Order = spell.order;
            ScriptParamsDataIds = spell.boundScriptUsageData.Select(x => x.Id).ToCSV(",");
            ScriptParamsCriticalIds = spell.criticalHitBoundScriptUsageData.Select(x => x.Id).ToCSV(",");
            IconId = spell.iconId;
            SpellLevelsIds = spell.spellLevels.ToArray();
            UseParamCache = spell.useParamCache;
            VerboseCast = spell.verboseCast;
            DefaultPreviewZone = spell.defaultPreviewZone;
            BypassSummoningLimit = spell.bypassSummoningLimit;
            CanAlwaysTriggerSpells = spell.canAlwaysTriggerSpells;
            AdminName = spell.adminName == "null" ? string.Empty : spell.adminName;
            HideCastConditions = spell.hideCastConditions;
        }

        #endregion

        #region ISaveIntercepter Members

        public void BeforeSave(bool insert)
        {
            m_spellLevelsIdsCSV = SpellLevelsIds.ToCSV(",");
        }

        #endregion

        public override string ToString()
        {
            return string.Format("{0} - {1}", Id, Name);
        }
    }
}