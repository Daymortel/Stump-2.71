using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;

namespace Stump.Server.WorldServer.Database.Monsters
{
    public class DungRusherRelator
    {
        public static string FetchQuery = "SELECT * FROM characters_rushers";
    }

    [TableName("characters_rushers")]
    public class DungRusherRecord : AutoAssignedRecord<DungRusherRecord>, IAutoGeneratedRecord
    {
        [PrimaryKey("Id")]
        public int Id { get; set; }

        public int DungeonId { get; set; }

        public string DungeonName { get; set; }

        public int OwnerId { get; set; }

        public string OwnerName { get; set; }

        public int OwnerLevel { get; set; }

        public double FightTime { get; set; }

        public void SetFightTime(double value)
        {
            FightTime = value;
            IsUpdate = true;
        }
    }
}