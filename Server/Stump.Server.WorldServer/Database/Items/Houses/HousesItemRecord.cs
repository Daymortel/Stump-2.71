using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Database.Items;

namespace Stump.Server.WorldServer.Database.World
{
    public class HousesItemRelator
    {
        public static string FetchQuery = "SELECT * FROM characters_coffer_houses_items";

        public static string FetchQueryByHoudeAndInteractive = "SELECT * FROM characters_coffer_houses_items WHERE HouseId = {0} AND InteractiveId = {1}";
    }

    [TableName("characters_coffer_houses_items")]
    public sealed class HousesItemRecord : ItemRecord<HousesItemRecord>, IAutoGeneratedRecord
    {
        public HousesItemRecord() { }

        public HousesItemRecord(int houseId, int interactiveId)
        {
            HouseId = houseId;
            InteractiveId = interactiveId;
        }

        public int HouseId { get; set; }

        public int InteractiveId { get; set; }

        public int GetHouseId()
        {
            return HouseId;
        }

        public int GetInteractiveId()
        {
            return InteractiveId;
        }
    }
}