using Stump.ORM;
using Stump.DofusProtocol.D2oClasses;
using Stump.DofusProtocol.D2oClasses.Tools.D2o;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Core.IO;
using System.Collections.Generic;
using System.Linq;

namespace Stump.Server.WorldServer.Database.Fights
{
    public class ChallengeRelator
    {
        public static string FetchQuery = "SELECT * FROM world_challenges";
    }

    [TableName("world_challenges")]
    [D2OClass("Challenge", "com.ankamagames.dofus.datacenter.challenges")]
    public class ChallengeRecord : IAssignedByD2O, IAutoGeneratedRecord
    {
        private List<uint> m_incompatibleChallenges;
        private string m_incompatibleChallengesCSV;

        [PrimaryKey("Id", false)]
        public int Id { get; set; }

        public string Name { get; set; }

        public uint NameId { get; set; }

        public uint DescriptionId { get; set; }

        [Ignore]
        public List<uint> IncompatibleChallenges
        {
            get { return m_incompatibleChallenges; }
            set
            {
                m_incompatibleChallenges = value;
                m_incompatibleChallengesCSV = value.ToCSV(",");
            }
        }

        public string IncompatibleChallengesCSV
        {
            get { return m_incompatibleChallengesCSV; }
            set
            {
                m_incompatibleChallengesCSV = value;
                m_incompatibleChallenges = value.FromCSV<uint>(",").ToList();
            }
        }

        public int CategoryId { get; set; }

        public int IconId { get; set; }

        public string ActivationCriterion { get; set; }

        public string CompletionCriterion { get; set; }

        public uint TargetMonsterId { get; set; }

        public void AssignFields(object obj)
        {
            var challenge = (Challenge)obj;

            Id = challenge.Id;
            Name = "";
            NameId = challenge.nameId;
            DescriptionId = challenge.descriptionId;
            IncompatibleChallengesCSV = challenge.IncompatibleChallenges.ToCSV(",");
            CategoryId = challenge.categoryId;
            IconId = challenge.iconId;
            ActivationCriterion = challenge.activationCriterion;
            CompletionCriterion = challenge.completionCriterion;
            TargetMonsterId = challenge.targetMonsterId;
        }
    }
}