using Stump.DofusProtocol.Enums;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Database.Npcs;
using Stump.Server.WorldServer.Game.Actors.RolePlay.Npcs;
using Stump.Server.WorldServer.Game.Maps;
using Stump.Server.WorldServer.Game.Maps.Cells;

namespace Stump.Server.WorldServer.Database.World
{
    public class WorldMapWantedRelator
    {
        public static string FetchQuery = "SELECT * FROM wanteds_spawns";
    }

    [TableName("wanteds_spawns")]
    public sealed class WatendSpawn : IAutoGeneratedRecord
    {
        private Map m_map;

        [PrimaryKey("WatendId")]
        public int WatendId { get; set; }

        public string Name { get; set; }

        public long? MapId { get; set; }

        [Ignore]
        public Map Map
        {
            get
            {
                if (!MapId.HasValue)
                    return null;

                return m_map ?? (m_map = Game.World.Instance.GetMap(MapId.Value));
            }
            set
            {
                m_map = value;

                if (value == null)
                    MapId = null;
                else
                    MapId = value.Id;
            }
        }

        public int CellId { get; set; }

        public DirectionsEnum Direction { get; set; }

        public int NpcId { get; set; }

        public int MonsterId { get; set; }
    }
}
