using Stump.Core.IO;
using Stump.DofusProtocol.D2oClasses.Tools.D2o;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Database.I18n;
using Stump.Server.WorldServer.Game.Maps;
using SubArea = Stump.DofusProtocol.D2oClasses.SubArea;
using Stump.Server.WorldServer.Database.Achievements;
using Stump.Server.WorldServer.Game.Achievements;
using Stump.Core.Reflection;

namespace Stump.Server.WorldServer.Database.World
{
    public class SubAreaRecordRelator
    {
        public static string FetchQuery = "SELECT * FROM world_subareas";
    }

    [TableName("world_subareas")]
    [D2OClass("SubArea", "com.ankamagames.dofus.datacenter.world")]
    public sealed class SubAreaRecord : IAssignedByD2O, ISaveIntercepter, IAutoGeneratedRecord
    {
        private uint[] m_customWorldMap;
        private string m_customWorldMapCSV;
        private uint[] m_mapsIds;
        private string m_mapsIdsCSV;
        private string m_name;
        private int[] m_shape;
        private string m_shapeCSV;
        private uint[] m_monsters;
        private string m_monstersCSV;
        private uint[] m_harvestables;
        private string m_harvestablesCSV;
        private AchievementTemplate m_achievement;
        private uint? m_achievementId;

        [PrimaryKey("Id", autoIncrement: false)]
        public int Id { get; set; }

        public uint NameId { get; set; }

        public string Name
        {
            get { return m_name ?? (m_name = TextManager.Instance.GetText(NameId)); }
            set { m_name = value; }
        }

        public int AreaId { get; set; }

        [Ignore]
        public uint[] MapsIds
        {
            get { return m_mapsIds; }
            set
            {
                m_mapsIds = value;
                m_mapsIdsCSV = value.ToCSV(",");
            }
        }

        public string MapsIdsCSV
        {
            get { return m_mapsIdsCSV; }
            set
            {
                m_mapsIdsCSV = value;
                m_mapsIds = value.FromCSV<uint>(",");
            }
        }

        [Ignore]
        public int[] Shape
        {
            get { return m_shape; }
            set
            {
                m_shape = value;
                m_shapeCSV = value.ToCSV(",");
            }
        }

        public string ShapeCSV
        {
            get { return m_shapeCSV; }
            set
            {
                m_shapeCSV = value;
                m_shape = value.FromCSV<int>(",");
            }
        }

        public int WorldmapId { get; set; }

        [Ignore]
        public uint[] CustomWorldMap
        {
            get { return m_customWorldMap; }
            set
            {
                m_customWorldMap = value;
                m_customWorldMapCSV = value.ToCSV(",");
            }
        }

        public string CustomWorldMapCSV
        {
            get { return m_customWorldMapCSV; }
            set
            {
                m_customWorldMapCSV = value;
                m_customWorldMap = value.FromCSV<uint>(",");
            }
        }

        public int PackId { get; set; }

        public uint Level { get; set; }

        public bool IsConquestVillage { get; set; }

        public bool BasicAccountAllowed { get; set; }

        public bool DisplayOnWorldMap { get; set; }

        public bool MountAutoTripAllowed { get; set; }

        public bool PsiAllowed { get; set; }

        [Ignore]
        public uint[] Monsters
        {
            get { return m_monsters; }
            set
            {
                m_monsters = value;
                m_monstersCSV = value.ToCSV(",");
            }
        }

        public string MonstersCSV
        {
            get { return m_monstersCSV; }
            set
            {
                m_monstersCSV = value;
                m_monsters = value.FromCSV<uint>(",");
            }
        }

        [DefaultSetting(0)]
        public bool Capturable { get; set; }

        [Ignore]
        public uint[] Harvestables
        {
            get { return m_harvestables; }
            set
            {
                m_harvestables = value;
                m_harvestablesCSV = value.ToCSV(",");
            }
        }

        public string HarvestablesCSV
        {
            get { return m_harvestablesCSV; }
            set
            {
                m_harvestablesCSV = value;
                m_harvestables = value.FromCSV<uint>(",");
            }
        }

        [DefaultSetting(2)]
        public Difficulty Difficulty { get; set; }

        [DefaultSetting(3)]
        public int SpawnsLimit { get; set; }

        [DefaultSetting(0)]
        public uint? CustomSpawnInterval { get; set; }

        [DefaultSetting(0)]
        public uint? AchievementId
        {
            get { return m_achievementId; }
            set
            {
                m_achievementId = value;
                m_achievement = value.HasValue ? Singleton<AchievementManager>.Instance.TryGetAchievement(value.Value) : null;
            }
        }

        [Ignore]
        public AchievementTemplate Achievement
        {
            get { return m_achievement; }
            set
            {
                m_achievement = value;
                m_achievementId = value == null ? new uint?() : m_achievement.Id;
            }
        }

        [DefaultSetting(0)]
        public int AssociatedZaapMapId { get; set; }

        #region IAssignedByD2O Members

        public void AssignFields(object d2oObject)
        {
            var subarea = (SubArea)d2oObject;

            Id = subarea.id;
            Name = "";
            NameId = subarea.nameId;
            AreaId = subarea.areaId;
            MapsIds = subarea.mapIds.ConvertAll(id => (uint)id).ToArray();
            Shape = subarea.shape.ToArray();
            WorldmapId = subarea.worldmapId;
            CustomWorldMap = subarea.customWorldMap.ToArray();
            PackId = subarea.packId;
            Level = subarea.Level;
            IsConquestVillage = subarea.IsConquestVillage;
            BasicAccountAllowed = subarea.basicAccountAllowed;
            DisplayOnWorldMap = subarea.displayOnWorldMap;
            MountAutoTripAllowed = subarea.mountAutoTripAllowed;
            PsiAllowed = subarea.psiAllowed;
            Monsters = subarea.monsters.ConvertAll(id => (uint)id).ToArray();
            Capturable = subarea.capturable;
            Harvestables = subarea.harvestables.ConvertAll(id => (uint)id).ToArray();
            Difficulty = Difficulty.Normal;
            SpawnsLimit = 3;
            CustomSpawnInterval = 0;
            AssociatedZaapMapId = subarea.AssociatedZaapMapId;
        }

        #endregion

        #region ISaveIntercepter Members

        public void BeforeSave(bool insert)
        {
            m_mapsIdsCSV = m_mapsIds.ToCSV(",");
            m_shapeCSV = m_shape.ToCSV(",");
            m_customWorldMapCSV = m_customWorldMap.ToCSV(",");
        }

        #endregion
    }
}