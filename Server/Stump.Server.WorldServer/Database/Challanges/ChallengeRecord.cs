using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.DofusProtocol.D2oClasses.Tools.D2o;
using Stump.Server.WorldServer.Database.I18n;
using Stump.Core.IO;

namespace Stump.Server.WorldServer.Database.Progression
{
    public class ChallengeRelator
    {
        public static string FetchQuery = "SELECT * FROM challenge";
    }

    [TableName("challenges")]
    [D2OClass("Challenge", "com.ankamagames.dofus.datacenter.challenges")]
    public class ChallengeRecord : IAssignedByD2O, IAutoGeneratedRecord
    {
        private string m_name;
        private uint[] m_incompatibleChallenges;
        private string m_incompatibleChallengesCSV;


        [PrimaryKey("Id", false)]
        public int Id { get; set; }

        public uint NameId { get; set; }

        public string Name
        {
            get { return m_name ?? (m_name = TextManager.Instance.GetText(NameId)); }
            set { m_name = value is null ? string.Empty : value; }
        }

        public uint DescriptionId { get; set; }

        [Ignore]
        public uint[] IncompatibleChallenges
        {
            get { return m_incompatibleChallenges ?? (m_incompatibleChallenges = m_incompatibleChallengesCSV.FromCSV<uint>(",")); }
            set
            {
                m_incompatibleChallenges = value;
                m_incompatibleChallengesCSV = value.ToCSV(",");
            }
        }

        public string IncompatibleChallengesCSV
        {
            get { return m_incompatibleChallengesCSV; }
            set
            {
                m_incompatibleChallengesCSV = value;
                m_incompatibleChallenges = m_incompatibleChallengesCSV.FromCSV<uint>(",");
            }
        }

        public int CategoryId { get; set; }

        public int IconId { get; set; }

        public string ActivationCriterion { get; set; }

        public string CompletionCriterion { get; set; }

        public uint TargetMonsterId { get; set; }

        #region IAssignedByD2O Members

        public void AssignFields(object d2oObject)
        {
            var activity = (DofusProtocol.D2oClasses.Challenge)d2oObject;

            Id = activity.Id;
            NameId = activity.NameId;
            Name = "";
            DescriptionId = activity.DescriptionId;
            IncompatibleChallenges = activity.IncompatibleChallenges.ToArray();
            CategoryId = activity.CategoryId;
            IconId = activity.IconId;
            ActivationCriterion = activity.ActivationCriterion;
            CompletionCriterion = activity.CompletionCriterion;
            TargetMonsterId = activity.TargetMonsterId;
        }

        #endregion
    }
}