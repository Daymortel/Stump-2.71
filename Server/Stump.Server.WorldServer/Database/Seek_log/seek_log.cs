using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using System;

namespace Database.Seeklog
{
    [TableName("characters_seek_logs")]
    public class seek_logRecord : IAutoGeneratedRecord
    {
        [PrimaryKey("Id")]
        public int Id { get; set; }     
        public DateTime Time { get; set; }
        public string Name_own { get; set; }
        public string Ip_own { get; set; }
        public int Honor_own { get; set; }
        public string Hardware_own { get; set; }
        public string Name_opponent { get; set; }
        public int Honor_opponent { get; set; }
        public string Ip_opponent { get; set; }
        public string Hardware_opponent { get; set; }

        [Ignore]
        public bool IsNew { get; set; }
    }
}
