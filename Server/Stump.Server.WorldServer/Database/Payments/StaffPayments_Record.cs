using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using System;

namespace Stump.Server.WorldServer.Database.Payments
{
    public class StaffPaymentsTableRelator
    {
        public static string FetchQuery = "SELECT * FROM staffs_payments";
    }

    [TableName("staffs_payments")]
    class StaffPayments : IAutoGeneratedRecord
    {
        [PrimaryKey("Id")]
        public int Id { get; set; }
        public string StaffName { get; set; }
        public string Name { get; set; }
        public string LastName { get; set; }
        public int AccountId { get; set; }
        public int PayDays { get; set; }
        public string Email { get; set; }
        public uint Wage { get; set; }
        public DateTime StartDate { get; set; }
        public DateTime LastPayment { get; set; }
        public uint Activo { get; set; }
    }
}
