using Stump.DofusProtocol.Types;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Game.Actors.Look;

namespace Stump.Server.WorldServer.Database.Presets
{
    public class CharacterPresetsRelator
    {
        public static string FetchQuery = "SELECT * FROM characters_presets";
        /// <summary>
        /// Use string.Format
        /// </summary>
        public static string FetchByOwner = "SELECT * FROM characters_presets WHERE OwnerId = {0}";
        public static string FetchByPresetId = "SELECT * FROM characters_presets WHERE PresetId = '{0}'";
        public static string FetchByObjectId = "SELECT * FROM characters_presets WHERE ObjectId = {0}";
    }

    [TableName("characters_presets")]
    public class CharacterPresetsRecord : IAutoGeneratedRecord
    {
        // Primitive properties

        [PrimaryKey("Id")]
        public int Id
        {
            get;
            set;
        }

        [Index]
        public int OwnerId
        {
            get;
            set;
        }

        public int ObjectId
        {
            get;
            set;
        }

        public int PresetId
        {
            get;
            set;
        }

        public int SymbolId
        {
            get;
            set;
        }

        public string Name
        {
            get;
            set;
        }

        public bool MountEquiped
        {
            get;
            set;
        }

        public string CharacterLook
        {
            get;
            set;
        }

        [Ignore]
        public EntityLook Look
        {
            get
            {
                return ActorLook.Parse(CharacterLook).GetEntityLook();
            }
        }

        
    }
}