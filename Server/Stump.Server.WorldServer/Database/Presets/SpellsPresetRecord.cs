using Stump.Core.IO;
using Stump.DofusProtocol.Types;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Game.Presets;

namespace Stump.Server.WorldServer.Database.Presets
{
    public class SpellsPresetsRelator
    {
        public static string FetchQuery = "SELECT * FROM characters_presets_spells";
        /// <summary>
        /// Use string.Format
        /// </summary>
        public static string FetchByPresetId = "SELECT * FROM characters_presets_spells WHERE PresetId={0}";
        public static string FetchByObjectId = "SELECT * FROM characters_presets_spells WHERE objectId={0}";
    }

    [TableName("characters_presets_spells")]
    public class SpellsPresetsRecord : IAutoGeneratedRecord
    {
        // Primitive properties

        private string m_SelectedSpellsIdsCSV;

        [PrimaryKey("Id")]
        public int Id
        {
            get;
            set;
        }

        [Index]
        public int OwnerId
        {
            get;
            set;
        }

        public int PresetId
        {
            get;
            set;
        }

        public ushort objectId
        {
            get;
            set;
        }

        public string SelectedSpellsIdsCSV
        {
            get
            {
                return m_SelectedSpellsIdsCSV;
            }
            set
            {
                m_SelectedSpellsIdsCSV = value;
            }
        }

        [Ignore]
        public SpellForPreset Spells
        {
            get
            {
                return PresetsManager.Instance.GetSpellsPreset(objectId, m_SelectedSpellsIdsCSV.FromCSV<short>(";"));
            }
            set
            {
                objectId = value.spellId;
                m_SelectedSpellsIdsCSV = value.shortcuts.ToCSV(";");
            }
        }
    }
}